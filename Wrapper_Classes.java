public class Wrapper_Classes {
    public static void main(String[] args) {
        

        // Wrapper Class of int is Integer
        Integer n1 = new Integer(10);
        Integer n2 = 15;
        System.out.println(n1);
        System.out.println(n2);

        // Wrapper Class of char is Character
        Character c1 = new Character('A');
        Character c2 = 'B';
        System.out.println(c1);
        System.out.println(c2);

        // Wrapper Class of float is Float
        Float f1 = new Float(10.5);
        Float f2 = 15.5f;
        System.out.println(f1);
        System.out.println(f2);

        // Wrapper Class of double is Double
        Double d1 = new Double(10.5);
        Double d2 = 15.5;
        System.out.println(d1);
        System.out.println(d2);

        // Wrapper Class of boolean is Boolean
        Boolean b1 = new Boolean(true);
        Boolean b2 = false;
        System.out.println(b1);
        System.out.println(b2);

    }
}
