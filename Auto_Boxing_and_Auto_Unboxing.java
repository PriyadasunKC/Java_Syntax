public class Auto_Boxing_and_Auto_Unboxing {
    public static void main(String[] args) {
        
        // Auto Boxing
        // Converting primitive data type to Wrapper Class
        Integer n1 = 10;
        Character c1 = 'A';
        Float f1 = 10.5f;
        Long l1 = 10l;
        Double d1 = 10.5;
        Boolean b1 = true;

        // Auto Unboxing
        // Converting Wrapper Class to primitive data type
        int n2 = n1;
        char c2 = c1;
        float f2 = f1;
        double d2 = d1;
        boolean b2 = b1;
    }
}
